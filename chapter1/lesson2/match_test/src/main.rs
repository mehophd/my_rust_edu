fn main() {
    let dice_roll = 4;

    match dice_roll {
        3 => println!("Выпало три"),
        7 => println!("Выпало семь"),
        other => println!("Выпало {}", other), // other — переменная-заполнитель
    }

    let mood = match dice_roll {
        1 => "Плохое",
        2 | 3 => "Нормальное",  // 2 ИЛИ 3
        4..=6 => "Отличное",    // Диапазон 4 до 6 включительно
        _ => "Неизвестное",     // _ = "любое другое значение"
    };
    
    println!("Настроение: {}", mood);
}

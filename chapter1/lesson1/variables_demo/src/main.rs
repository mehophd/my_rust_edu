fn main() {
    // Целые числа
    let integer: i32 = 42;
    let unsigned: u32 = 100;

    // Числа с плавающей точкой
    let float: f64 = 3.14;

    // Булевы значения
    let boolean: bool = true;

    // Символы
    let character: char = 'A';

    // Строки
    let string: &str = "Rust - это круто!";

    // Вывод всех значений
    println!("Целое число: {}", integer);
    println!("Беззнаковое: {}", unsigned);
    println!("Вещественное: {}", float);
    println!("Булево: {}", boolean);
    println!("Символ: {}", character);
    println!("Строка: {}", string);
}
